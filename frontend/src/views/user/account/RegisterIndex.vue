
<script setup>
import { ref } from "vue";
import { useUserStore } from "@/stores/user.js";
import { useRouter } from "vue-router";
import api from "@/js/http/api.js";
const username = ref('')
const password = ref('')
const passwordConfirmed = ref('')
const errorMessage = ref('')

const user = useUserStore()
const router = useRouter()

async function handleRegister(){
  errorMessage.value = ''
  if (!username.value.trim()){
    errorMessage.value = '用户名不能为空'
  } else if (!password.value.trim()){
    errorMessage.value = "密码不能为空"
  } else if (password.value !== passwordConfirmed.value){
    errorMessage.value = '两次密码输入不一致'
  } else {
    try{
      const res = await api.post('/api/user/account/register/',{
        username: username.value,
        password: password.value,
      })
      const data = res.data
      if(data.result === 'success'){
        user.setAccessToken(data.access)
        user.setUserInfo(data)
        await router.push({
          name: 'homepage-index'
        })
      } else {
        errorMessage.value = data.result
      }
    } catch(err){
      console.log(err)
    }
  }
 }
</script>

<template>
<div class = "flex justify-center mt-30">
  <form @submit.prevent="handleRegister" class="fieldset bg-base-200 border-base-300 rounded-box w-xs border p-4">
  <legend class="fieldset-legend">Register</legend>

  <label class="label">输入用户名</label>
  <input v-model = "username" type="text" class="input" placeholder="Email" />

  <label class="label">输入密码</label>
  <input v-model = "password" type="password" class="input" placeholder="Password" />


    <label class="label">确认密码</label>
  <input v-model = "passwordConfirmed" type="password" class="input" placeholder="Password" />

  <p v-if = "errorMessage" class = "text-sm text-red-500 mt-1">{{ errorMessage }}</p>
  <button class="btn btn-neutral mt-4">注册</button>
  <div class = "flex justify-end">
    <router-link :to = "{ name : 'user-account-login-index'}" class = "btn btn-sm btn-ghost text-gray-500" >
      登录
    </router-link>

  </div>
  </form>
</div>


</template>


